export var ProficiencyLevel;
(function (ProficiencyLevel) {
    ProficiencyLevel[ProficiencyLevel["Trainee"] = 1] = "Trainee";
    ProficiencyLevel[ProficiencyLevel["Basic"] = 2] = "Basic";
    ProficiencyLevel[ProficiencyLevel["Intermediate"] = 3] = "Intermediate";
    ProficiencyLevel[ProficiencyLevel["Advanced"] = 4] = "Advanced";
    ProficiencyLevel[ProficiencyLevel["Expert"] = 5] = "Expert";
})(ProficiencyLevel || (ProficiencyLevel = {}));
